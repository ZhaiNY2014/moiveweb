package com.bm.web.model;

public class BuyLog {
	private int logid;
	private String boughtmovie;
	private String boughtuser;
	
	public int getLogid() {
		return logid;
	}
	public void setLogid(int logid) {
		this.logid = logid;
	}
	public String getBoughtmovie() {
		return boughtmovie;
	}
	public void setBoughtmovie(String boughtmovie) {
		this.boughtmovie = boughtmovie;
	}
	public String getBoughtuser() {
		return boughtuser;
	}
	public void setBoughtuser(String boughtuser) {
		this.boughtuser = boughtuser;
	}
	
}
